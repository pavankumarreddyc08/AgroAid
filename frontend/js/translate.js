// тФАтФАтФА Change language тФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФА
function changeLanguage(lang) {
    if (!translations[lang]) return; // guard against unknown codes
    currentLanguage = lang;
    localStorage.setItem('agroaidLanguage', lang);

    const el = document.getElementById('currentLang');
    if (el) el.textContent = langLabels[lang];

    document.getElementById('languageDropdown').classList.remove('active');
    translatePage();
}

// тФАтФАтФА Apply translations to the page тФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФА
function translatePage() {
    const lang = translations[currentLanguage];
    if (!lang) return;

    // Text content тАФ also handles input[placeholder] tagged with data-translate
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (lang[key] !== undefined) {
            if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
                el.placeholder = lang[key];
            } else {
                el.textContent = lang[key];
            }
        }
    });

    // Explicit placeholder attributes (legacy support)
    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
        const key = el.getAttribute('data-translate-placeholder');
        if (lang[key] !== undefined) {
            el.placeholder = lang[key];
        }
    });
}

// тФАтФАтФА Initialise on DOMContentLoaded тФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФА
document.addEventListener('DOMContentLoaded', function () {
    const el = document.getElementById('currentLang');
    if (el) el.textContent = langLabels[currentLanguage] || 'EN';

    translatePage();

    const languageBtn      = document.getElementById('languageBtn');
    const languageDropdown = document.getElementById('languageDropdown');

    if (languageBtn && languageDropdown) {
        languageBtn.addEventListener('click', function (e) {
            e.stopPropagation();
            languageDropdown.classList.toggle('active');
        });

        document.addEventListener('click', function (e) {
            if (!languageBtn.contains(e.target) && !languageDropdown.contains(e.target)) {
                languageDropdown.classList.remove('active');
            }
        });
    }
});